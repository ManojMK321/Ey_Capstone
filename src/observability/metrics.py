# Context Managers for Timing
# ============================================================================

class TimerContext:
    """Context manager for recording operation duration."""

    def __init__(self, histogram: Histogram, labels: Optional[dict] = None):
        self.histogram = histogram
        self.labels = labels or {}
        self.start_time = None

    def __enter__(self):
        self.start_time = time.perf_counter()
        return self

    def __exit__(self, exc_type, exc_val, exc_tb):
        elapsed = time.perf_counter() - self.start_time
        self.histogram.labels(**self.labels).observe(elapsed)


class CounterContext:
    """Context manager for incrementing/decrementing active request counters."""

    def __init__(self, inc_counter: Gauge, dec_counter: Optional[Gauge] = None, labels: Optional[dict] = None):
        self.inc_counter = inc_counter
        self.dec_counter = dec_counter
        self.labels = labels or {}

    def __enter__(self):
        self.inc_counter.labels(**self.labels).inc()
        return self

    def __exit__(self, exc_type, exc_val, exc_tb):
        if self.dec_counter:
            self.dec_counter.labels(**self.labels).dec()


@contextlib.contextmanager
def timed_llm_call(model: str, operation: str = "complete"):
    """
    Wrap a single LLM API call:

        with timed_llm_call(self.model, operation="answer"):
            response = self.client.responses.create(...)

    Records latency on success, `llm_call_errors_total` on exception
    (re-raised unchanged either way).
    """
    start = time.perf_counter()
    try:
        yield
    except Exception as exc:
        record_llm_call(model=model, duration=time.perf_counter() - start, operation=operation, error=type(exc).__name__)
        raise
    else:
        record_llm_call(model=model, duration=time.perf_counter() - start, operation=operation)


@contextlib.contextmanager
def timed_agent_step(agent_type: str, step_name: str):
    """
    Wrap one logical step of an agent pipeline:

        with timed_agent_step("agentic_rag", "retrieve"):
            retrieved = self.retrieve(subquestions)

    Records duration + invocation status (success/error) via record_agent_step.
    """
    start = time.perf_counter()
    try:
        yield
    except Exception:
        record_agent_step(agent_type=agent_type, step_name=step_name, duration=time.perf_counter() - start, status="error")
        raise
    else:
        record_agent_step(agent_type=agent_type, step_name=step_name, duration=time.perf_counter() - start, status="success")


@contextlib.contextmanager
def timed_db_query(query_type: str, operation: str):
    """
    Wrap a database operation:

        with timed_db_query("postgres", "append_history"):
            ...

    Records latency always, and increments db_errors_total on exception
    (re-raised unchanged).
    """
    start = time.perf_counter()
    try:
        yield
    except Exception as exc:
        DB_ERRORS.labels(query_type=query_type, error_type=type(exc).__name__).inc()
        raise
    finally:
        DB_QUERY_DURATION_SECONDS.labels(query_type=query_type, operation=operation).observe(time.perf_counter() - start)


